# operadores aritmeticos
x = 10
y = 5
# suma +
print('la suma de x + y es:', x + y)
# resta -
print('la resta de x - y es:', x - y)
# multiplicacion *
print('la multiplicacion de x * y es:', x * y)
# division /
print('la division de x / y es:', x / y)
# division entera //
print('la division entera de x // y es:', x // y)
# modulo %
print('el modulo de x % y es:', x % y)
# potencia **
print('la potencia de x ** y es:', x ** y)
# operadores de comparacion
# igual ==
print('x es igual a y:', x == y)
# distinto !=
print('x es distinto a y:', x != y)
# mayor que >
print('x es mayor que y:', x > y)
# menor que <
print('x es menor que y:', x < y)
# mayor o igual que >=
print('x es mayor o igual que y:', x >= y)
# menor o igual que <=
print('x es menor o igual que y:', x <= y)
# operadores logicos
# and
print('x es mayor que 5 y menor que 20:', x > 5 and x < 20)
# or
print('x es mayor que 5 o menor que 20:', x > 5 or x < 20)
# not
print('x no es mayor que 5:', not x > 5)    
# operadores de identidad
# is
print('x es y:', x is y)
# is not
print('x no es y:', x is not y)
# operadores de pertenencia
# in
print('x esta en la lista [1, 2, 3]:', x in [1, 2, 3])
# not in
print('x no esta en la lista [1, 2, 3]:', x not in [1, 2, 3])
# operadores de bits
# and
print('x and y:', x & y)
str1 = 3
str2 = 3
print('str1 and str2:', str1 & str2)    
# or
print('x or y:', x | y)
# xor
print('x xor y:', x ^ y)